package Seventh;

public class Question3 {
    public static void main(String[] args) {
        System.out.println("2^2 = " + 2 * 2);
        System.out.println("2^2 = " + Math.pow(2, 2));

        System.out.println("2^3 = " + 2 * 2 * 2);
        System.out.println("2^2 = " + Math.pow(2, 3));

        System.out.println("2^10 = " + 2 * 2 * 2 * 2 * 2 * 2 * 2 * 2 * 2 * 2);
        System.out.println("2^10 = " + Math.pow(2, 10));
    }
}
